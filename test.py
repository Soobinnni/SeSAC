# p.125 : 사용자에게 데이터를 입력받아 inch단위를 cm단위로 가공한 후 결과 출력.
inch= int(input('변환할 숫자를 입력하세요(inch)'))
cm = inch * 2.54

print('입력숫자 :', inch, '\n변환 숫자 :', cm,"cm입니다.")
